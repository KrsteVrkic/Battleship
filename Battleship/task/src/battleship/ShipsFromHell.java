package battleship;

public class ShipsFromHell {
    public int cells;
    public String name;

    public ShipsFromHell(int cells, String name) {
        this.cells = cells;
        this.name = name;
    }

    public int getShipCells() {
        return cells;
    }

    public String getShipName() {
        return name;
    }

}
